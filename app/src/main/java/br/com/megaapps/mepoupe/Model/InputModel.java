public class InputModel {

    private String resultCode;
    private String id;
    private String value;
    private String date;
    private String category;
    private String description;
    private String totalExpense;
    private String totalRecipe;
    private String totalObjective;
    private String totalMonth;

    public String getResultCode() {
        return resultCode;
    }

    public void setResultCode(String resultCode) {
        this.resultCode = resultCode;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getTotalExpense() {
        return totalExpense;
    }

    public void setTotalExpense(String totalExpense) {
        this.totalExpense = totalExpense;
    }

    public String getTotalRecipe() {
        return totalRecipe;
    }

    public void setTotalRecipe(String totalRecipe) {
        this.totalRecipe = totalRecipe;
    }

    public String getTotalObjective() {
        return totalObjective;
    }

    public void setTotalObjective(String totalObjective) {
        this.totalObjective = totalObjective;
    }

    public String getTotalMonth() {
        return totalMonth;
    }

    public void setTotalMonth(String totalMonth) {
        this.totalMonth = totalMonth;
    }
}
